# LOOPS MEDIANTE SETS
# Python nos permite crear bucles a través de un set con el keyword for

print('LOOPS THROUGH SET') # Muestro título.
colors = {'red', 'green', 'blue'} # Creo var set y sus items.
for items in colors: # Creo var 'item' para que tome el valor de cada elemento en la var 'colors'
  print(items) # Muestro var 'item'. Será 'red' 'blue' green'.
  
  
